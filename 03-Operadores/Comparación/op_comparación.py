# ⚖️ 2. Operadores relacionales (comparación)

# Sirven para comparar valores.
# El resultado siempre es booleano (True o False).

# Operador	Significado
# ==	igual
# !=	diferente
# >	mayor
# <	menor
# >=	mayor o igual
# <=	menor o igual

x = 10
y = 5

print(x > y)
print(x < y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)