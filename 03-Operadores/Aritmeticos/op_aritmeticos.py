# #1. Operadores aritméticos
# #Sirven para hacer operaciones matemáticas.

# #Operador	Significado	Ejemplo
# +	suma	5 + 3
# -	resta	5 - 3
# *	multiplicación	5 * 3
# /	división	10 / 2
# //	división entera	10 // 3
# %	módulo (residuo)	10 % 3
# **	potencia	2 ** 3

a = 10
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)