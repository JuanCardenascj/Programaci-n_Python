"""Funciones con parámetros.!
   
   Los parámetros permiten que la función reciba datos..!"""

#Ejemplo.!
def saludar(nombre): #Para llamar el parametro
    print(f"Hola, {nombre}")
saludar("Juan") #Para darle valor a ese parametro!
saludar("Silvia Cárdenas")


#Ejemplo2.!
def sumar(a, b):
    print(a + b)
sumar(12, 5)