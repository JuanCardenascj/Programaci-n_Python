#Función - INT! Permite convertir una cadena numérica a un valor entero
numero = "123"
print(int(numero))

#Función - INT! Permite convertir un float a un entero
numero1 = 123.2
print(int(numero1))

#Función - Float! Permite convertir una cadena numérica a un dato float
numero2 = "1234233"
print(float(numero2))

#Función - Float! Permite convertir un entero a un float
numero3 = 923
print(float(numero3))

#Función - STR! Permite convertir un dato a tipo cadena
numero4 = 4312
print(str(numero4))