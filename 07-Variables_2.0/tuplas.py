"""Las tuplas:
    Son viables de crear cuando solo tenemos datos de solo lectura
    
    Por ejemplo: Cuando solo se van a leer datos"""

#Creando tuplas con tuple()
tupla = tuple(["Dato1", "Dato2"])

#Creando una tupla sin parentesis mutiple
tupla = "Daniel", "David"

#Creando una tupla con un dato
tupla = "datosolo",

print(tupla)